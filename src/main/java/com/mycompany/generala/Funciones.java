package com.mycompany.generala;

public class Funciones {
    public static int OneTwosThree(int d1, int d2, int d3, int d4, int d5, int x){
        int sum=0;
        if (d1 == x) sum += x;
        if (d2 == x) sum += x;
        if (d3 == x) sum += x;
        if (d4 == x) sum += x;
        if (d5 == x) sum += x;
        return sum;
    }
}
